public class Calculator {

    public int adunare(int first, int second) {
        return first + second;
    }
    public int scadere (int first, int second) {
        return first - second;
    }
    public int inmultire (int first, int second) {
        return first * second;
    }
    public int impartire ( int first, int second){
        return first / second;
    }
}
